// Description: Les calcules classiques +-*/
public class Calculs {
    public static void main(String[] args) {
        System.out.println("---- Les calcules classiques +-*/ ----");
        int a = 5;
        int b = 3;
        int c = a + b;
        System.out.println(String.valueOf(a) + " + " + String.valueOf(b) + " = " + c);
        c = a - b;
        System.out.println(String.valueOf(a) + " - " + String.valueOf(b) + " = " + c);
        c = a * b;
        System.out.println(String.valueOf(a) + " * " + String.valueOf(b) + " = " + c);
        c = a / b;
        System.out.println(String.valueOf(a) + " / " + String.valueOf(b) + " = " + c);
    }
}
